package kv.pojo.bo.excel;

public class RefConeBO {
    private String beamId;
    private String beamIdAlt;
    private String nomEnergy;
    private String measSsd;
    private String measDepth;
    private String geomShape;
    private String geomSize;
    private String closedOpen;
    private String thickness;
    private String dospSsdZ;

    public String getBeamId() {
        return beamId;
    }

    public void setBeamId(String beamId) {
        this.beamId = beamId;
    }

    public String getBeamIdAlt() {
        return beamIdAlt;
    }

    public void setBeamIdAlt(String beamIdAlt) {
        this.beamIdAlt = beamIdAlt;
    }

    public String getNomEnergy() {
        return nomEnergy;
    }

    public void setNomEnergy(String nomEnergy) {
        this.nomEnergy = nomEnergy;
    }

    public String getMeasSsd() {
        return measSsd;
    }

    public void setMeasSsd(String measSsd) {
        this.measSsd = measSsd;
    }

    public String getMeasDepth() {
        return measDepth;
    }

    public void setMeasDepth(String measDepth) {
        this.measDepth = measDepth;
    }

    public String getGeomShape() {
        return geomShape;
    }

    public void setGeomShape(String geomShape) {
        this.geomShape = geomShape;
    }

    public String getGeomSize() {
        return geomSize;
    }

    public void setGeomSize(String geomSize) {
        this.geomSize = geomSize;
    }

    public String getClosedOpen() {
        return closedOpen;
    }

    public void setClosedOpen(String closedOpen) {
        this.closedOpen = closedOpen;
    }

    public String getThickness() {
        return thickness;
    }

    public void setThickness(String thickness) {
        this.thickness = thickness;
    }

    public String getDospSsdZ() {
        return dospSsdZ;
    }

    public void setDospSsdZ(String dospSsdZ) {
        this.dospSsdZ = dospSsdZ;
    }
}
