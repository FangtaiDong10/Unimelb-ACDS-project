package kv.pojo.bo.excel;

public class BeamQualityBO {
    private String beamId;
    private String scd;
    private String fieldSizeAtScd;
    private String hvlNominalMmAl;
    private String hvlNominalMmCu;
    private String hvlMeasuredMmAl;
    private String hvlMeasuredMmCu;

    public String getBeamId() {
        return beamId;
    }

    public void setBeamId(String beamId) {
        this.beamId = beamId;
    }

    public String getScd() {
        return scd;
    }

    public void setScd(String scd) {
        this.scd = scd;
    }

    public String getFieldSizeAtScd() {
        return fieldSizeAtScd;
    }

    public void setFieldSizeAtScd(String fieldSizeAtScd) {
        this.fieldSizeAtScd = fieldSizeAtScd;
    }

    public String getHvlNominalMmAl() {
        return hvlNominalMmAl;
    }

    public void setHvlNominalMmAl(String hvlNominalMmAl) {
        this.hvlNominalMmAl = hvlNominalMmAl;
    }

    public String getHvlNominalMmCu() {
        return hvlNominalMmCu;
    }

    public void setHvlNominalMmCu(String hvlNominalMmCu) {
        this.hvlNominalMmCu = hvlNominalMmCu;
    }

    public String getHvlMeasuredMmAl() {
        return hvlMeasuredMmAl;
    }

    public void setHvlMeasuredMmAl(String hvlMeasuredMmAl) {
        this.hvlMeasuredMmAl = hvlMeasuredMmAl;
    }

    public String getHvlMeasuredMmCu() {
        return hvlMeasuredMmCu;
    }

    public void setHvlMeasuredMmCu(String hvlMeasuredMmCu) {
        this.hvlMeasuredMmCu = hvlMeasuredMmCu;
    }
}
